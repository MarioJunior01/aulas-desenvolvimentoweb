        <h1>Sobre o Projeto "Juntos pelo Planeta"</h1>

        <h2>História do "Juntos pelo Planeta"</h2>
        <p>O "Juntos pelo Planeta" foi fundado com o objetivo de unir pessoas que desejam fazer a diferença no combate às questões ambientais. Surgiu a partir da necessidade urgente de ações concretas para preservar o meio ambiente e garantir um futuro mais sustentável para as próximas gerações. Desde o seu início, a missão do projeto é incentivar práticas ecológicas, promover a conscientização e envolver a comunidade em atividades que impactem positivamente o nosso planeta.</p>

        <h2>Objetivos do Projeto</h2>
        <p>O principal objetivo do "Juntos pelo Planeta" é criar uma rede de voluntários e cidadãos engajados na luta pela preservação ambiental. Através de ações coletivas e organizadas, buscamos:</p>
        <ul>
            <li><strong>Reduzir o impacto ambiental:</strong> Promover atividades de conscientização e preservação, como o plantio de árvores e a limpeza de áreas públicas.</li>
            <li><strong>Envolver as comunidades locais:</strong> Incentivar as pessoas a se envolverem em ações práticas para melhorar a qualidade do ambiente ao seu redor.</li>
            <li><strong>Educar para a sustentabilidade:</strong> Oferecer informações e recursos sobre práticas sustentáveis que podem ser adotadas no cotidiano.</li>
        </ul>

        <h2>Missão do Projeto</h2>
        <p>A missão do "Juntos pelo Planeta" é ser um agente de transformação ambiental, promovendo o engajamento e a conscientização de um número crescente de pessoas em relação às questões ecológicas. Buscamos fortalecer a conexão entre os indivíduos e o meio ambiente, incentivando atitudes responsáveis que contribuam para a preservação dos recursos naturais. Nosso trabalho visa transformar o planeta em um lugar mais saudável e sustentável para todos.</p>

